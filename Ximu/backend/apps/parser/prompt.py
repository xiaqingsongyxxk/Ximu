"""简历解析器使用的提示词模板模块。

本模块定义了发送给LLM的提示词：
1. SYSTEM - 系统提示词，定义AI角色和输出格式
2. CONTENT - 用户提示词模板，包含要解析的简历文本

提示词是LLM理解任务的关键，好的提示词能提高解析准确率。
"""  # 模块文档字符串

# 系统提示词：定义AI的角色、行为规则和输出格式
SYSTEM = """\
你是一个专业的简历解析器。分析提供的上下文并将其提取为标准化的简历数据结构。

# 核心规则
- 只返回有效的 JSON 内容，不要包含任何额外的描述或解释性文本。
- 对于上下文中不存在的信息，使用空字符串 ""。
- 所有日期必须遵循 YYYY-MM 格式。缺失的字段也使用 ""。
- 对于当前在职情况，设置 current=true 且 end_date=""。
- 从每个部分提取每一个条目——每一份工作经历、每一个项目、每一段教育背景、每一个证书和每一门语言。不要合并或省略任何条目。如果简历中有 3 个项目，就在 projects 数组中返回 3 个对象。如果有 5 份工作经历，就在 work_experiences 数组中返回 5 个对象。
- 仔细阅读文档的所有页面。关键信息可能分布在多个页面上。

下面是您必须严格遵循的 JSON Schema 定义：
---
{json_schema}
---"""

